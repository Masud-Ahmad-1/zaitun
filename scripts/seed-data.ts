const TAGS_POOL = ["স্মৃতি", "ছেলেবেলা", "গ্রাম", "পরিবার", "ঐতিহ্য", "ইসলাম", "শিক্ষা", "কাজ", "ভ্রমণ", "উৎসব", "মা", "বাবা", "দাদা", "নদী", "বৃষ্টি", "খাবার", "স্কুল", "মসজিদ", "বিয়ে", "ঈদ"];

function rand(min: number, max: number) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function generateDate(year: number): string {
  const month = String(rand(1, 12)).padStart(2, "0");
  const day = String(rand(1, 28)).padStart(2, "0");
  return `${year}-${month}-${day}`;
}

async function main() {
  console.log("=== Starting seed: 127 families with public diaries ===\n");

  // Create a few dummy users to be tree creators
  const dummyUsers = [];
  const userNames = [
    "রহিম উদ্দিন", "করিম মিয়া", "আলী হোসেন", "গোলাম মোস্তফা",
    "নজরুল ইসলাম", "আব্দুল হালিম", "শফিকুল ইসলাম", "মোশাররফ হোসেন",
    "আমিনুল ইসলাম", "শামসুল হক",
  ];

  for (let i = 0; i < userNames.length; i++) {
    const name = userNames[i];
    const email = `seed.user${i + 1}@zaitun.local`;
    const user = await db.user.upsert({
      where: { email },
      create: {
        email,
        name,
        password: "$2b$10$dummyhashedpassword000000000000000000000000000000000000000",
        role: "member",
        locale: "bn",
      },
      update: {},
    });
    dummyUsers.push(user);
    console.log(`  User: ${name} (${email})`);
  }
  console.log(`\n  Created ${dummyUsers.length} users\n`);

  // Create 127 public family trees
  const trees = [];
  const usedFamilyNames = new Set<string>();

  for (let i = 0; i < 127; i++) {
    let familyName = pick(FAMILY_NAMES);
    // Ensure uniqueness with a number suffix if needed
    let attempt = 0;
    while (usedFamilyNames.has(familyName) && attempt < 20) {
      familyName = pick(FAMILY_NAMES);
      attempt++;
    }
    usedFamilyNames.add(familyName);

    const creator = pick(dummyUsers);
    const suffix = usedFamilyNames.size <= FAMILY_NAMES.length ? " পরিবার" : "";
    const treeName = `${familyName}${suffix}`;

    // Generate a unique invite code
    const code = `ZAITUN-${String(i + 1).padStart(4, "0")}-${String.fromCharCode(65 + (i % 26))}${String.fromCharCode(65 + ((i * 3) % 26))}`;

    try {
      const tree = await db.familyTree.create({
        data: {
          name: treeName,
          description: `${familyName} পরিবারের বংশবৃত্তি ও ইতিহাস`,
          isPrivate: false,
          inviteCode: code,
          createdBy: creator.id,
        },
      });
      trees.push(tree);

      // Add creator as member
      await db.familyTreeMember.create({
        data: { treeId: tree.id, userId: creator.id, role: "owner" },
      });

      // Create 2-6 persons per tree
      const personCount = rand(2, 6);
      for (let j = 0; j < personCount; j++) {
        const isMale = Math.random() > 0.45;
        const firstName = isMale ? pick(MALE_NAMES) : pick(FEMALE_NAMES);
        const lastName = familyName;
        const gender = isMale ? "male" : "female";
        const occupation = pick(OCCUPATIONS);
        const birthYear = rand(1920, 2005);
        const isDeceased = birthYear < 1960 && Math.random() > 0.5;
        const deathYear = isDeceased ? rand(birthYear + 40, 2020) : null;

        const person = await db.person.create({
          data: {
            treeId: tree.id,
            firstName,
            lastName,
            gender,
            occupation,
            birthDate: `${birthYear}-01-01`,
            deathDate: deathYear ? `${deathYear}-06-15` : null,
            isDeceased,
            bio: `${firstName} ${lastName} ${familyName} পরিবারের একজন সম্মানিত সদস্য। পেশায় ${occupation}।`,
            sortOrder: j,
          },
        });

        // Add 0-3 diary entries per person (public)
        const diaryCount = rand(0, 3);
        for (let k = 0; k < diaryCount; k++) {
          const title = pick(DIARY_TITLES_BN);
          const content = pick(DIARY_CONTENTS_BN);
          const year = rand(birthYear + 10, 2024);
          const tags = Array.from({ length: rand(1, 3) }, () => pick(TAGS_POOL))
            .filter((v, idx, arr) => arr.indexOf(v) === idx)
            .join(",");

          await db.diaryEntry.create({
            data: {
              treeId: tree.id,
              personId: person.id,
              date: generateDate(year),
              title,
              content,
              privacy: "public",
              tags,
            },
          });
        }

        // Link first person of first tree to first user via TreeMember (for demo)
        if (i === 0 && j === 0) {
          await db.treeMember.create({
            data: { treeId: tree.id, userId: creator.id, role: "owner" },
          }).catch(() => {});
        }
      }

      if ((i + 1) % 10 === 0) {
        console.log(`  Created ${i + 1}/127 trees...`);
      }
    } catch (e: any) {
      // Skip on unique constraint violation
      if (String(e).includes("Unique")) {
        console.log(`  Skipping duplicate tree: ${treeName}`);
        continue;
      }
      throw e;
    }
  }

  // Count results
  const totalTrees = await db.familyTree.count({ where: { isPrivate: false } });
  const totalPersons = await db.person.count();
  const totalDiaries = await db.diaryEntry.count({ where: { privacy: "public" } });

  console.log(`\n=== Seed Complete ===`);
  console.log(`  Total public trees: ${totalTrees}`);
  console.log(`  Total persons: ${totalPersons}`);
  console.log(`  Total public diaries: ${totalDiaries}`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => db.$disconnect());